package Method;

public class GönderilenİkiSayınınÇarpımınıDöndürenMetot {

    public static void main(String[] args) {

        int sayi1=15;
        int sayi2=25;

        System.out.println("\nİki sayının çarpımı: "+ikiSayininCarpimiMethodu(sayi1, sayi2));



    }

    private static int ikiSayininCarpimiMethodu(int sayi1, int sayi2) {
        return sayi1*sayi2;
    }
}
